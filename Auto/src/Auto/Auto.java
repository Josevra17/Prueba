package Auto;


public class Auto {
    private String marca;
    private String modelo;
    private int caro;
    private double klm;
    
    //Setters

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public void setCaro(int caro) {
        this.caro = caro;
    }

    public void setKlm(double klm) {
        this.klm = klm;
    }
    
    //Getters

    public String getMarca() {
        return marca;
    }

    public String getModelo() {
        return modelo;
    }

    public int getCaro() {
        return caro;
    }

    public double getKlm() {
        return klm;
    }
    
    
    
}
